package Problems_1_10;

public class checkEvenOdd {
    public static void main(String[] args) {

        int num = 3231;
        if (num % 2 == 0) {
            System.out.println("Entered Number is Even: "+num);
        }else {
            System.out.println("Entered Numbers is Odd: "+num);
        }
    }
}
